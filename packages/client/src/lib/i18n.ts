let currentLang: Language = "en";

export function setLanguage(lang: Language): void {
	currentLang = lang;
}

export function getLanguage(): Language {
	return currentLang;
}

export function t(key: string, lang?: Language): string {
	const l = lang ?? currentLang;
	return TRANSLATIONS[l]?.[key] ?? TRANSLATIONS.en[key] ?? key;
}

/** Get the language name for AI prompts (e.g., "Respond in Korean") */
export function getLanguageInstruction(): string {
	const names: Record<Language, string> = {
		en: "", // No instruction needed for English
		ko: "Respond in Korean (한국어).",
		es: "Respond in Spanish (Español).",
		vi: "Respond in Vietnamese (Tiếng Việt).",
		mn: "Respond in Mongolian (Монгол).",
	};
	return names[currentLang];
}

